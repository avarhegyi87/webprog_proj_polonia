    „Cyberstwory” (Kiberszörnyek) című vetélkedő végeredményét.</p>
<p>A rendezvény a Lengyel Nagykövetség mellett működő Lengyel Iskolában lesz, amelynek címe: 1025 Budapest, Törökvész út
    15.</p>
