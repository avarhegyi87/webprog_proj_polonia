<h3 class="arch">A Lem emlékév fináléja</h3>
<p>A Polonia Nova Kulturális Egyesület közösen a Lengyel Nagykövetség mellett működő Lengyel Iskolával szeretettel
    várnak minden érdeklődőt a LEM-OFF.2021-re, vagyis a Lem emlékév fináléjára, amely november 3-án 18.00-kor lesz
